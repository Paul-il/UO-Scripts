from staff import ingots,deadra,box,hammer,zulu_color,logs,kryss,bow
from datetime import datetime
from py_stealth import *

def repair_kryss():
    Disarm()
    MoveXYZ(2851,174,26,True,1,True)
    UseObject(box)
    FindType(hammer,box)
    Grab(FindItem(),1)
    Wait(200)
    UseType2(hammer)
    Wait(200)
    if FindTypeEx(ingots,deadra,box,True):
        Grab(FindItem(),100)
    Wait(200)
    if TargetPresent():
        CancelTarget()
    MoveXYZ(2852,176,26,True,1,True)
    UseType2(hammer)
    Wait(200)
    FindType(kryss)
    WaitTargetObject(FindItem())
    WaitGump(161)
    Wait(3000)
    MoveXYZ(2849,174,26,True,1,True)
    Disarm()
    Wait(500)
    UseObject(box)
    FindType(hammer,Backpack())
    MoveItem(FindItem(),1,box,0,0,0)
    Wait(500)
    FindType(ingots,Backpack())
    MoveItem(FindItem(),-1,box,0,0,0)
    Wait(500)
    
